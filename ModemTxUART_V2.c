void txInit_V2()
{
	pitInit();
	idle = true;
	num_bit = 0;
	num_punto = 0;
	buff[0] = 0xFF;
	buff[1] = 0xFF;
	ind_buff = 0;
	ind_tab = 0;
	PWM_init(PWM_PTA1, &pinID);
	SendBits();
	SendBits();
	pitSetAndBegin(PIT_1, TIME0);
	pitSetAndBegin(PIT_3, TIME1);
	pitSetIRQFunc(PIT_1, SendBits);
	pitSetIRQFunc(PIT_3, SendSig);

}

bool txSend_V2(unsigned char word)
{
	if(buff[0] == 0xFF)
	{
		buff[0] = ProtocolConstruct(word);
		idle = false;
		return true;
	}
	else if(buff[1] == 0xFF)
	{
		buff[1] = ProtocolConstruct(word);
		idle = false;
		return true;
	}
	else
	{
		return false;
	}
}

bool txBufferComplete_V2(void)
{
	if((buff[0] == 0xFF) && (buff[1] == 0xFF))
	{
		return true;
	}
	else
	{
		return false;
	}

}



/*******************************************************************************
 *******************************************************************************
                        LOCAL FUNCTION DEFINITIONS
 *******************************************************************************
 ******************************************************************************/
static uint16_t ProtocolConstruct(unsigned char val)
{
	uint16_t aux = 0;
	aux = val;
	aux = (aux << 1);
	uint8_t par = 0;
	for(int i = 0; i<9; i++)
	{
		par += ((val >> i) & MASK);
	}
	par = (par % 2 == 0) ? 0 : 1;
	aux |= (par << 9) | (1 << 10);
	return aux;
}

static void SendBits()
{
	uint8_t bit;
	bit = (buff[ind_buff] >> num_bit) & MASK;		//me quedo solamente con el bit correspondiente a esta interrupcion
	int aux = (ind_tab + 1) & 1;
	modulador_cpfsk(bit, F_UNO, F_CERO,
				F_BAUD, F_SALIDA, tabs[aux], senoidal);
	num_bit++;
	if (num_bit == CANT_BITS) 					//termino de mandar todos los bits de la palabra, tengo que
	{												//limpiar el buffer que la contenia y reiniciar el indice
		num_bit = 0;
		buff[ind_buff] = 0xFF;
		if((buff[0] == 0xFF) && (buff[1] == 0xFF))
		{
			idle = true;
		}
		ind_buff = (ind_buff + 1) & 1;
	}
	else if(idle)		//sigue en 0 hasta salir del estado idle
	{
		num_bit = 0;
	}
}

static void SendSig()		//manda cada lugar de la tabla correspondiente al pwm
{
	gpioToggle(PORTNUM2PIN(PE,26));
	PWM_setDutyPinWithCnV(&pinID, tabs[ind_tab][num_punto]);
	num_punto++;
	if(num_punto == CANT_PUNTOS)
	{
		num_punto = 0;
		ind_tab = (ind_tab + 1) & 1;
	}

}
